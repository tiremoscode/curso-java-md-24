public class CalculadoraJessicaRamirez {
    public static void main(String[] args) {
        //Variables donde haré el calculo.
        int numero1 = 30;
        int numero2= 50;

        //Suma
        int suma= numero1 + numero2;
        System.out.println("El valor de la suma "+suma);

        //Resta
        int resta= numero1 - numero2;
        System.out.println("El valor de la resta "+resta);

        //Multiplicación 
        int Multiplicación= numero1 * numero2;
        System.out.println("El valor de la multiplicación " +Multiplicación);

        // División
        int division= numero1 / numero2;
        System.out.println("El valor de la división "+division);

        
    }
}
